    private void jButtonGRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGRelatorioActionPerformed
        if (("" + jComboBoxTipoPesquisa.getSelectedItem()).equals("Por Data")) {
            String Data = jFormattedTextFieldData.getText();
            Venda V = new Venda();
            V.PesquisaData(Data, jTableRelatorio);
        } else {
            String Data1 = jFormattedTextFieldData.getText();
            String Data2 = jFormattedTextFieldData2.getText();
            Venda V = new Venda();
            V.PesquisaEntreData(Data1, Data2, jTableRelatorio);
        }

    }//GEN-LAST:event_jButtonGRelatorioActionPerformed

    private void jComboBoxTipoPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxTipoPesquisaActionPerformed
        if (("" + jComboBoxTipoPesquisa.getSelectedItem()).equals("Selecione Tipo de Pesquisa")) {
            jComboBoxTipoPesquisa.setEnabled(false);
            jButtonGRelatorio.setEnabled(false);
            jFormattedTextFieldData.setEnabled(false);
            jFormattedTextFieldData2.setEnabled(false);
            jLabelA.setText("");
        } else if (("" + jComboBoxTipoPesquisa.getSelectedItem()).equals("Por Data")) {
            jButtonGRelatorio.setEnabled(true);
            jFormattedTextFieldData.setEnabled(true);
            jFormattedTextFieldData2.setEnabled(false);
            jLabelA.setText("");
        } else {
            jButtonGRelatorio.setEnabled(true);
            jFormattedTextFieldData.setEnabled(true);
            jFormattedTextFieldData2.setEnabled(true);
            jLabelA.setText("Ã ");
        }

    }//GEN-LAST:event_jComboBoxTipoPesquisaActionPerformed

