    private void jButtonRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRetirarActionPerformed
        // TODO add your handling code here:
        if (jTableProduto.getSelectedRow() != -1) {
            int Qtd;
            Qtd = Integer.parseInt(JOptionPane.showInputDialog("Informe a quantidade de produtos retirados"));
            Produto P = new Produto();
            P.setCodProduto(Integer.parseInt("" + jTableProduto.getValueAt(jTableProduto.getSelectedRow(), 0)));
            P.PesquisaCodigo();

            if ((P.getQtdProduto() - Qtd) > 0) {
                P.setQtdProduto(P.getQtdProduto() - Qtd);
                P.Alterar();
                P.carregaTabela(jTableProduto);
            } else {
                JOptionPane.showMessageDialog(this, "Estoque insuficiente");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um produto");
        }
    }//GEN-LAST:event_jButtonRetirarActionPerformed

