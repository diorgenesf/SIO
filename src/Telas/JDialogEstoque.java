    private void jButtonComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonComprarActionPerformed
        // TODO add your handling code here:
        if (jTableProduto.getSelectedRow() != -1) {
            int cod = Integer.parseInt("" + jTableProduto.getValueAt(jTableProduto.getSelectedRow(), 0));
            Float Valor = Float.parseFloat("" + jTableProduto.getValueAt(jTableProduto.getSelectedRow(), 2));
            ProdutoFornecido PF = new ProdutoFornecido();
            PF.setCodProduto(cod);
            PF.PesquisaCodigoProduto();
            Fornecedor F = new Fornecedor();
            F.setCNPJ(PF.getCNPJFornecedor());
            F.PesquisaPorCNPJ();
            jTextFieldRazaoFornecedor.setText(F.getRazaoSocial());
            int quant = Integer.parseInt(JOptionPane.showInputDialog("Entre com quantidade:"));
            new JDialogPagamentoCP(this, true, PF.getCNPJFornecedor(), cod,quant, quant*Valor).setVisible(true);
            Produto P = new Produto();
            P.setCodProduto(cod);
            P.PesquisaCodigo();
            P.setQtdProduto(P.getQtdProduto() + quant);
            P.Alterar();
            Produto.carregaTabela(jTableProduto);
            JOptionPane.showMessageDialog(this, "Estoque Atualizado com sucesso");
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um produto");
        }
    }//GEN-LAST:event_jButtonComprarActionPerformed

