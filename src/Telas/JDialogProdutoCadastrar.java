    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        
        Produto c1 = new Produto();
        c1.setCodProduto(Integer.parseInt(jTextFieldCodigo.getText()));
        c1.setDescProduto(jTextFieldDescProduto.getText());
        c1.setEstoqueMinimo(Integer.parseInt(jTextFieldEstoqueMinimo.getText()));
        c1.setObservacao(jTextAreaObservacao.getText());
        c1.setQtdProduto(Integer.parseInt(jTextFieldQuantidade.getText()));
        c1.setValorUnitario(Float.parseFloat(jTextFieldValorUnitario.getText())); 
        c1.Inserir();
        
        ProdutoFornecido PF = new ProdutoFornecido();
        Fornecedor F = new Fornecedor();
        F.setRazaoSocial((String)jComboBoxFornecedor.getSelectedItem());
        F.PesquisaPorRazao();
        PF.setCNPJFornecedor(F.getCNPJ());
        PF.setCodProduto(Integer.parseInt(jTextFieldCodigo.getText()));
        PF.setDataFornecimento(DateTime.GetDate());
        PF.Inserir();
        
        new JDialogPagamentoCP(this, true, F.getCNPJ(), Integer.parseInt(jTextFieldCodigo.getText()),
                Integer.parseInt(jTextFieldQuantidade.getText()),
                Integer.parseInt(jTextFieldValorUnitario.getText())*Integer.parseInt(jTextFieldQuantidade.getText())).setVisible(true);
        
        jTextFieldCodigo.setText("");
        jTextFieldDescProduto.setText("");
        jTextFieldValorUnitario.setText("");
        jTextAreaObservacao.setText("");
        jComboBoxFornecedor.removeAllItems();
        F.carregaCombo(jComboBoxFornecedor);
        jTextFieldQuantidade.setText("");
        jTextFieldEstoqueMinimo.setText("10");
        jTextFieldData.setText(DateTime.GetDate(DateTime.GetDate()));
        JOptionPane.showMessageDialog(this, "Produto Cadastrado com Sucesso!");
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

